# math
import math

# pi
print(math.pi)

# e
print(math.e)

# sqrt()
print(math.sqrt(4))

# ceil()
print(math.ceil(3.5))

# floor()
print(math.floor(3.5))

# pow()
print(math.pow(2, 3.4))

# fabs()
print(math.fabs(-3.2))

# sin()
print(math.sin(math.pi))

# cos()
print(math.cos(math.pi))

# tan()
print(math.tan(math.pi))

# random
import random

# random()
print(random.random())

# randint()
print(random.randint(0, 10))

# randrange()
print(random.randrange(0, 10))

# statistics
import statistics

# mean()
print(statistics.mean([1, 1, 2, 3]))

# median()
print(statistics.median([1, 1, 2, 3]))

# mode()
print(statistics.mode([1, 1, 2, 3]))
